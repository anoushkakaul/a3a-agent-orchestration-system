# ---------------- Batch run: create 20 transcripts and save a single JSON ----------------
import requests, json, time, uuid, os, pprint, datetime

# Settings: change if needed
NUM_EXAMPLES = 20
START_DATE_BASE = "2025-10-15"   # base date; we'll offset days per example
OUT_FILE = "batch_itineraries.json"
TIMEOUT = 40

# Make sure required globals exist
try:
    WEATHER_PORT
    PLANNER_PORT
except NameError:
    # fallback to env or defaults used earlier
    WEATHER_PORT = int(os.environ.get('WEATHER_PORT', WEATHER_PORT if 'WEATHER_PORT' in globals() else 8102))
    PLANNER_PORT = int(os.environ.get('PLANNER_PORT', PLANNER_PORT if 'PLANNER_PORT' in globals() else 8103))

# sample cities (20). Edit this list if you want specific cities.
cities = [
    "Mumbai","Delhi","Bengaluru","Chennai","Kolkata",
    "Hyderabad","Pune","Ahmedabad","Jaipur","Lucknow",
    "Surat","Nagpur","Visakhapatnam","Bhopal","Coimbatore",
    "Thiruvananthapuram","Patna","Ludhiana","Vadodara","Goa"
]

# if fewer than NUM_EXAMPLES, repeat/rotate
if len(cities) < NUM_EXAMPLES:
    cities = (cities * ((NUM_EXAMPLES // len(cities)) + 1))[:NUM_EXAMPLES]
else:
    cities = cities[:NUM_EXAMPLES]

batch_results = []

def iso_date_offset(base_iso, offset_days):
    # base_iso expected YYYY-MM-DD
    dt = datetime.datetime.strptime(base_iso, "%Y-%m-%d")
    dt2 = dt + datetime.timedelta(days=offset_days)
    return dt2.strftime("%Y-%m-%d")

for idx, city in enumerate(cities, start=1):
    req_id = str(uuid.uuid4())
    start_date = iso_date_offset(START_DATE_BASE, idx % 7)  # vary start date a bit
    n_days = 2  # or choose 2/3 randomly
    prefs = {"budget": "moderate", "interests": ["sightseeing", "food"]}

    transcript = {
        "request_id": req_id,
        "created_at": datetime.datetime.utcnow().isoformat() + "Z",
        "user_payload": {"city": city, "start_date": start_date, "n_days": n_days, "preferences": prefs},
        "weather_calls": [],
        "planner_call": None
    }

    print(f"[{idx}/{NUM_EXAMPLES}] Processing {city} starting {start_date} ...")

    # 1) Query WeatherAgent structured for each date and record
    weather_structured_list = []
    for di in range(n_days):
        date = f"{start_date}+{di}"
        wreq = {"jsonrpc":"2.0","id": str(uuid.uuid4()), "method":"get_weather_structured", "params":{"city": city, "date": date}}
        headers = {"Content-Type":"application/json", "X-A2A-KEY": os.environ.get('A2A_API_KEY')}

        call_record = {"date": date, "sent_at": datetime.datetime.utcnow().isoformat() + "Z", "request": wreq}
        try:
            r = requests.post(f"http://127.0.0.1:{WEATHER_PORT}/rpc", json=wreq, headers=headers, timeout=10)
            call_record["status_code"] = r.status_code
            try:
                call_record["response"] = r.json()
                # try to extract structured block if the agent returns it under result.structured
                resp_json = call_record["response"]
                structured = None
                if isinstance(resp_json, dict) and resp_json.get("result"):
                    if isinstance(resp_json["result"], dict) and resp_json["result"].get("structured"):
                        structured = resp_json["result"]["structured"]
                    elif isinstance(resp_json["result"], dict) and resp_json["result"].get("text"):
                        structured = {"text": resp_json["result"]["text"]}
                call_record["extracted_structured"] = structured
                weather_structured_list.append(resp_json)
            except Exception as e:
                call_record["response_text"] = r.text
                weather_structured_list.append({"error": "invalid-json-response", "text": r.text})
        except Exception as e:
            call_record["error"] = str(e)
            weather_structured_list.append({"error": str(e)})
        call_record["response_received_at"] = datetime.datetime.utcnow().isoformat() + "Z"
        transcript["weather_calls"].append(call_record)
        # small pause to avoid tight loops
        time.sleep(0.3)

    # 2) Call PlannerAgent with the preloaded structured weather list
    planner_payload = {
        "jsonrpc":"2.0",
        "id": req_id,
        "method":"plan_from_structured",
        "params": {
            "city": city,
            "start_date": start_date,
            "n_days": n_days,
            "preferences": prefs,
            "weather_structured": weather_structured_list
        }
    }
    headers = {"Content-Type":"application/json", "X-A2A-KEY": os.environ.get('A2A_API_KEY')}
    planner_record = {"sent_at": datetime.datetime.utcnow().isoformat() + "Z", "request": planner_payload}
    try:
        pr = requests.post(f"http://127.0.0.1:{PLANNER_PORT}/rpc", json=planner_payload, headers=headers, timeout=TIMEOUT)
        planner_record["status_code"] = pr.status_code
        try:
            planner_record["response"] = pr.json()
        except Exception:
            planner_record["response_text"] = pr.text
    except Exception as e:
        planner_record["error"] = str(e)
    planner_record["response_received_at"] = datetime.datetime.utcnow().isoformat() + "Z"
    transcript["planner_call"] = planner_record

    # 3) Save transcript to list
    batch_results.append(transcript)

    # optional small delay between examples
    time.sleep(0.6)

# Save entire batch file
with open(OUT_FILE, "w") as f:
    json.dump(batch_results, f, indent=2)

print("Batch saved to:", OUT_FILE)
print("Number of transcripts saved:", len(batch_results))
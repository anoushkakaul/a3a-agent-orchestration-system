def make_agent_card(name, version, port, methods):
    card = {
        "agent_id": f"{name.lower()}-{str(uuid.uuid4())[:8]}",
        "name": name,
        "version": version,
        "endpoint": f"http://127.0.0.1:{port}/rpc",
        "methods": methods
    }
    # also write to disk for discovery
    with open(f"agent_card_{name.lower()}.json", "w") as f:
        json.dump(card, f, indent=2)
    return card

def require_auth(headers):
    key = headers.get('x-a2a-key') or headers.get('X-A2A-KEY') or headers.get('X-A2A-Key')
    return key == A2A_KEY

def jsonrpc_success(id_, result):
    return {"jsonrpc":"2.0","id": id_,"result": result}

def jsonrpc_error(id_, code, message):
    return {"jsonrpc":"2.0","id": id_,"error": {"code": code,"message": message}}


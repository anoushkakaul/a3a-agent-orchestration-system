# simple helper: call agent JSON-RPC
def call_agent(endpoint, method, params):
    payload = {"jsonrpc":"2.0","id": str(uuid.uuid4()), "method": method, "params": params}
    headers = {"Content-Type":"application/json","X-A2A-KEY": os.environ['A2A_API_KEY']}
    r = requests.post(endpoint, json=payload, headers=headers, timeout=10)
    return r.json()


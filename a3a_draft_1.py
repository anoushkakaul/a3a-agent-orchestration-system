    body = await req.json()
    # auth
    if not require_auth(req.headers):
        return jsonrpc_error(body.get("id"), -32600, "unauthorized: missing/invalid X-A2A-KEY")
    method = body.get("method")
    params = body.get("params", {})
    rid = body.get("id")
